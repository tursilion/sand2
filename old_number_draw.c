void drawnum(int c, int x, int y) {
    int xx, yy;
    
    for (yy=0; yy<5; yy++) {
        for (xx=0; xx<3; xx++) {
            if (chr[c][yy][xx] == '.') {
                VideoBuf[(yy+y)*240+xx+x]=0xffff;
            } else {
                VideoBuf[(yy+y)*240+xx+x]=0;
            }
        }
    }
};

void drawhex(int a, int x, int y) {
	drawnum((a>>28)&0xf, 0, y);
	drawnum((a>>24)&0xf, 4, y);
	drawnum((a>>20)&0xf, 8, y);
	drawnum((a>>16)&0xf,12, y);
	drawnum((a>>12)&0xf,16, y);
	drawnum((a>>8)&0xf,20, y);
	drawnum((a>>4)&0xf,24, y);
	drawnum((a)&0xf,28, y);
}
